#!/usr/bin/env node
/**
 * Generates CLAUDE.md and AGENTS.md at the repo root from
 *   .agent-rules/source.md   (top-level orientation)
 *   .claude/rules/*.md       (modular topic rules, alphabetical order)
 *
 * Simpler than tankloop's: no path-scoped leaf AGENTS.md (Rove's repo
 * doesn't have enough depth to need them yet), no frontmatter, no codex-
 * specific filtering. Add those when the repo grows.
 *
 * Run via `pnpm sync:agent-guides` or directly via node.
 */
import { readFileSync, readdirSync, writeFileSync } from "node:fs";
import { resolve } from "node:path";

const repoRoot = resolve(import.meta.dirname, "..");
const sourcePath = resolve(repoRoot, ".agent-rules/source.md");
const rulesDir = resolve(repoRoot, ".claude/rules");

const HEADER =
  "<!-- Generated by scripts/sync-agent-guides.mjs from .agent-rules/source.md and .claude/rules/. " +
  "Do not edit. Run: pnpm sync:agent-guides -->";

function loadRules() {
  return readdirSync(rulesDir)
    .filter((f) => f.endsWith(".md"))
    .sort()
    .map((f) => ({
      filename: f,
      body: readFileSync(resolve(rulesDir, f), "utf8").trimEnd(),
    }));
}

function renderSection(rule) {
  return `## Rule · ${rule.filename}\n\n${rule.body}`;
}

function build() {
  const source = readFileSync(sourcePath, "utf8").trimEnd();
  const rules = loadRules();
  const sections = [HEADER, "", source];
  if (rules.length > 0) {
    sections.push("", "---", "", "# Repo-wide rules");
    for (const r of rules) sections.push("", renderSection(r));
  }
  sections.push("");
  return sections.join("\n");
}

function main() {
  const content = build();
  for (const filename of ["CLAUDE.md", "AGENTS.md"]) {
    const out = resolve(repoRoot, filename);
    writeFileSync(out, content, "utf8");
    console.log(`✓ wrote ${filename} (${content.length} bytes)`);
  }
}

main();
